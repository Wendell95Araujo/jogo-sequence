function translate(key, params = {}) {
  let str;
  if (key.includes(".")) {
    const keys = key.split(".");
    str =
      keys.reduce((obj, i) => obj?.[i], translations[currentLanguage]) ??
      keys.reduce((obj, i) => obj?.[i], translations["en"]) ??
      key;
  } else {
    str =
      translations[currentLanguage?.toLowerCase()]?.[key] ??
      translations["en"]?.[key] ??
      key;
  }

  if (typeof str === "string") {
    for (const param in params) {
      str = str.replace(new RegExp(`{${param}}`, "g"), params[param]);
    }
  }
  return str;
}

function initializeTranslations() {
  const savedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY) || (navigator.language.split("-")[0] || 'pt');
  currentLanguage = ["pt", "en", "es"].includes(savedLang) ? savedLang : 'pt';
  document.documentElement.lang = currentLanguage;

  document.title = translate("pageTitle");
  const descriptionTag = document.querySelector('meta[name="description"]');
  if (descriptionTag) {
    descriptionTag.setAttribute("content", translate("pageDescription"));
  }

  $("[data-translate-key]").each(function () {
    const key = $(this).data("translate-key");
    $(this).html(translate(key));
  });

  $("[data-translate-placeholder-key]").each(function () {
    const key = $(this).data("translate-placeholder-key");
    $(this).attr("placeholder", translate(key));
  });

  $("[data-translate-title-key]").each(function () {
    const key = $(this).data("translate-title-key");
    $(this).attr("title", translate(key));
  });

  $("[data-translate-aria-label-key]").each(function () {
    const key = $(this).data("translate-aria-label-key");
    $(this).attr("aria-label", translate(key));
  });
}
